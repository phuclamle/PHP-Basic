<?php 
// Hàm strtolower: chuyển tất cả kí tự sang chữ viết thường
$chuoi9 = "Hienu";
echo strtolower($chuoi9)."</br>";

// Hàm strtoupper: chuyển tất cả kí tự sang viết hoa
$chuoi10 = 'hienu';
echo strtoupper($chuoi10);

// Hàm ucfirst: chuyển chữ cái đầu tiên sang viết hoa
echo ucfirst($chuoi10)."</br>";

// Hàm lcfirst: chuyển chữ cái đầu tiên sang dạng viết thường
$chuoi11 = "HIENU";
echo lcfirst($chuoi11)."</br>";
